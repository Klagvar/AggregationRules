package aggregation.kemeny;

/**
 * Режим работы адаптивного метода Кемени.
 */
public enum AdaptiveWeightMode {
    
    /**
     * Фокус на конфликтах: больший вес позициям, где эксперты расходятся во мнениях.
     * 
     * φ(k) = H(k) / H_max
     * 
     * Логика: Где эксперты спорят — там важно найти компромисс.
     */
    CONFLICT_FOCUS,
    
    /**
     * Фокус на консенсусе: больший вес позициям, где эксперты согласны.
     * 
     * φ(k) = 1 - H(k) / H_max
     * 
     * Логика: Где эксперты согласны — там больше уверенности в результате.
     */
    CONSENSUS_FOCUS
}

